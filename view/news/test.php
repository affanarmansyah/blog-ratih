1. buat array yang berisi angka 1 - 10 dengan metode for
<br>
<?php
$angka = [];

// Loop untuk mengisi array dengan angka 1-10
for ($i = 1; $i <= 10; $i++) {
    $angka[] = $i;
}
print_r($angka);

// Cetak isi array
?>

